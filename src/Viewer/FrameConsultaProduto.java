    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) jTableProdutos.getModel();

        if (jTableProdutos.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "selecione um item!");
        } else {
            try {

                if (ClassDados.getListaProduto().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Lista Vazia!");
                } else {
                    for (int i = 0; i < ClassDados.getListaProduto().size(); i++) {
                        ClassProduto classProduto = ClassDados.getListaProduto().get(i);
                        if ((String.valueOf(modelo.getValueAt(jTableProdutos.getSelectedRow(), 0)).equals(String.valueOf(classProduto.getCodigoProduto())))) {
                            ClassProduto classproduto = new ClassProduto(
                                    Integer.parseInt(jTextFieldCodigoProduto.getText()),
                                    jTextFieldDescricao.getText(),
                                    Double.parseDouble(jTextFieldValorUnitario.getText()),
                                    Integer.parseInt(jTextFieldQuantidade.getText()));
                            ClassDados.getListaProduto().set(i, classproduto);
                            JOptionPane.showMessageDialog(null, "alteracao com Sucesso!");
                            jTextFieldCodigoProduto.setText("");
                            jTextFieldDescricao.setText("");
                            jTextFieldQuantidade.setText("");
                            jTextFieldValorUnitario.setText("");
                            modelo.setNumRows(0);
                            CarregaProduto();
                        } else {
                            JOptionPane.showMessageDialog(null, "Produto nao Cadastrado");
                        }
                    }
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Erro: Campos Invalidos! " + e.getMessage());
            }

        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

