    private void jButtonConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultaActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTableProdutos.getModel();
        modelo.setNumRows(0);
        try {
            if (!jTextFieldCodigoProduto.getText().trim().isEmpty()) {
                for (ClassProduto classproduto : ClassDados.getListaProduto()) {
                    if (jTextFieldCodigoProduto.getText().trim().equals(String.valueOf(classproduto.getCodigoProduto()))) {
                        modelo.addRow(new Object[]{
                            classproduto.getCodigoProduto(),
                            classproduto.getValor(),
                            classproduto.getDescricao(),
                            classproduto.getQtdEstoque()});
                    }

                }

            } else {
                if (!jTextFieldDescricao.getText().trim().isEmpty()) {
                    for (ClassProduto classproduto : ClassDados.getListaProduto()) {
                        if (classproduto.getDescricao().contains(jTextFieldDescricao.getText().trim().toUpperCase())) {
                            modelo.addRow(new Object[]{
                                classproduto.getCodigoProduto(),
                                classproduto.getValor(),
                                classproduto.getDescricao(),
                                classproduto.getQtdEstoque()});
                        }

                    }
                } else {
                    for (ClassProduto classproduto : ClassDados.getListaProduto()) {

                        modelo.addRow(new Object[]{
                            classproduto.getCodigoProduto(),
                            classproduto.getValor(),
                            classproduto.getDescricao(),
                            classproduto.getQtdEstoque()});

                    }

                }

            }
        } catch (NumberFormatException E) {
            JOptionPane.showMessageDialog(null, "Erro Codigo invalido! " + E.getMessage());
        }
    }//GEN-LAST:event_jButtonConsultaActionPerformed

