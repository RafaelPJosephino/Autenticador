    private void selecionarProduto(int linha) {
        DefaultTableModel modelo = (DefaultTableModel) jTableProdutos.getModel();
        jTextFieldCodigoProduto.setText(String.valueOf(modelo.getValueAt(linha, 0)));
        jTextFieldValorUnitario.setText(String.valueOf(modelo.getValueAt(linha, 1)));
        jTextFieldDescricao.setText((String) modelo.getValueAt(linha, 2));
        jTextFieldQuantidade.setText(String.valueOf(modelo.getValueAt(linha, 3)));

    }

    private void CarregaProduto() {
        DefaultTableModel modelo = (DefaultTableModel) jTableProdutos.getModel();
        modelo.setNumRows(0);
        jTableProdutos.getColumnModel().getColumn(0).setPreferredWidth(20);
        jTableProdutos.getColumnModel().getColumn(1).setPreferredWidth(20);
        jTableProdutos.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTableProdutos.getColumnModel().getColumn(3).setPreferredWidth(20);

    }

